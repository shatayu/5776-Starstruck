task lift() {
	// 0 for when off
	// 0.01 for claw open
	// 0.015 for claw closed

	int stall = 0;

	float proportional = 0.01;
	float weightDef = 0.01;
	float weightSet = 0.01;

	while (true) {
		if (vexRT[Btn6U]) {
			moveLift(127);
		} else if (vexRT[Btn6D]) {
			moveLift(-127);
		} else if (SensorValue(LiftPot) < 800) {
			moveLift(10);
		} else if (SensorValue(LiftPot) < 2300) {
			stall = (2800 - SensorValue(LiftPot)) * proportional;
			moveLift(stall);
		} else if (SensorValue(LiftPot) > 2800) {
			stall = (2400 - SensorValue(LiftPot)) * proportional;
			moveLift(stall);
		} else {
			moveLift(0);
		}

		wait1Msec(20);
	}
}

task usercontrol() {
	Toggle claw;
	claw.state = 0;
	claw.buffer = 0;

	startTask(lift);

	while (true) {
		// arcade drive
		moveDrive(vexRT[Ch3] + vexRT[Ch1], vexRT[Ch3] - vexRT[Ch1]);

		// claw
		if (vexRT[Btn5D] && (vexRT[Btn5D] != claw.buffer))  // if button is pressed and it was not pressed before
	   	claw.state = claw.state ? 0 : 1;

		claw.buffer = vexRT[Btn5D];
		clawState(claw.state);

		// transmission
		if (vexRT[Btn8U])
			transmissionState(OPEN);

		if (vexRT[Btn8D])
			transmissionState(CLOSED);

		if (vexRT[Btn8R])
			autonLiftUp(2900);

		if (vexRT[Btn8L])
			autonLiftUp(2000);

		wait1Msec(20);
	}
}
